public class CastingtipePrimitif {
    public static void main(String[] args) {
        
        double valDouble = 10.12;
        int valInt=(int)valDouble; //mengkonvert valDouble ke tipe int

        double x = 10.2;
        int y = 2;

        int result = (int)(x/y);

        System.out.println(valInt);
        System.out.println(result);
    }
}
